#include "philo.h"

long	get_time(void)
{
	static struct timeval	time;

	gettimeofday(&time, NULL);
	return ((time.tv_sec * 1000) + (time.tv_usec / 1000));
}

void	ft_sleep(long times)
{
	time_t	start_time;

	start_time = get_time();
	while (get_time() - start_time < times)
		usleep(100);
}
